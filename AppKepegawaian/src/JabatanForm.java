    // ðŸ”¹ Load Data Jabatan ke JTable
    private void loadData() {
        model.setRowCount(0);
        try (Connection con = Koneksi.getKoneksi()) {
            Statement stmt = con.createStatement();
            ResultSet rs = stmt.executeQuery("SELECT * FROM jabatan");
            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("id_jabatan"),
                    rs.getString("nama_jabatan"),
                    rs.getDouble("gaji")
                });
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

    // ðŸ”¹ Tambah Jabatan
    private void tambahData() {
        try (Connection con = Koneksi.getKoneksi()) {
            String sql = "INSERT INTO jabatan (nama_jabatan, gaji) VALUES (?, ?)";
            PreparedStatement ps = con.prepareStatement(sql);
            ps.setString(1, txtNamaJabatan.getText());
            ps.setDouble(2, Double.parseDouble(txtGaji.getText()));
            ps.executeUpdate();
            JOptionPane.showMessageDialog(this, "Jabatan Berhasil Ditambahkan!");
            loadData();
            resetForm();
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

    // ðŸ”¹ Edit Jabatan
    private void editData() {
        try (Connection con = Koneksi.getKoneksi()) {
            String sql = "UPDATE jabatan SET nama_jabatan=?, gaji=? WHERE id_jabatan=?";
            PreparedStatement ps = con.prepareStatement(sql);
            ps.setString(1, txtNamaJabatan.getText());
            ps.setDouble(2, Double.parseDouble(txtGaji.getText()));
            ps.setInt(3, Integer.parseInt(txtIdJabatan.getText()));
            ps.executeUpdate();
            JOptionPane.showMessageDialog(this, "Jabatan Berhasil Diperbarui!");
            loadData();
            resetForm();
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

    // ðŸ”¹ Hapus Jabatan
    private void hapusData() {
        try (Connection con = Koneksi.getKoneksi()) {
            String sql = "DELETE FROM jabatan WHERE id_jabatan=?";
            PreparedStatement ps = con.prepareStatement(sql);
            ps.setInt(1, Integer.parseInt(txtIdJabatan.getText()));
            ps.executeUpdate();
            JOptionPane.showMessageDialog(this, "Jabatan Berhasil Dihapus!");
            loadData();
            resetForm();
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

    // ðŸ”¹ Reset Form
    private void resetForm() {
        txtIdJabatan.setText("");
        txtNamaJabatan.setText("");
        txtGaji.setText("");
    }

